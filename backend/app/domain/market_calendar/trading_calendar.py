def _is_trading_day(day: date, holidays: frozenset[date]) -> bool:
    return day.weekday() < 5 and day not in holidays


def previous_trading_day(day: date, *, market: str) -> date:
    holidays = BR_B3_HOLIDAYS if market == "br" else US_NYSE_HOLIDAYS
    candidate = day - timedelta(days=1)
    while not _is_trading_day(candidate, holidays):
        candidate -= timedelta(days=1)
    return candidate
